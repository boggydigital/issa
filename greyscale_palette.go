func GreyscalePalette() color.Palette {
	plt := color.Palette{}
	for ii := 0; ii < 256; ii++ {
		c := uint8(ii)
		plt = append(plt, color.RGBA{R: c, G: c, B: c, A: 255})
	}
	return plt
}
